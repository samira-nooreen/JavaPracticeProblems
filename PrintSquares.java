/*                           Print Squares

Write a program that utilizes a while loop to print the squares of numbers from 1 to 5.
Sample 1:
Input
Output        1 4 9 16 25                   */

class Codechef
{
	public static void main (String[] args) 
	{
		// your code goes here
		int x = 1;
		while(x <= 5 ){
		    System.out.println(x*x);
		    x++;
		}

	}
}
